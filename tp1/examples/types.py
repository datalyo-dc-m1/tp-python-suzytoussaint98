# Les types simples

# On déclare nos variables
a_string = "string"  # une chaîne de caractères
an_integer = 2  # un entier
a_float = 3.0  # un décimal
a_boolean = True  # un booléen
unknown = None  # une variable non affectée

# On affiche la valeur nos variables à l'aide de la fonction print()
print(a_string)
print(an_integer)
print(a_float)
print(a_boolean)
print(unknown)

# On peut connaître le type de nos variables à l'aide de la fonction type()
print(type(a_string))
print(type(an_integer))
print(type(a_float))
print(type(a_boolean))
print(type(unknown))

# Convertir le type d'une variable
print(str(True))  # en chaîne de caractère
print(int(1.2))  # en entier
print(float("1"))  # en décimal
print(bool(0))  # en booléen
