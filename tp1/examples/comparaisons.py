# Les opérateurs de comparaisons

x = 1
y = 2

print(x == y)  # vérifie si x est égal à y
print(x != y)  # vérifie si x est différent de y

print(x > y)  # vérifie si x est strictement supérieur à y
print(x < y)  # vérifie si x est strictement inférieur à y

print(x >= y)  # vérifie si x est supérieur ou égal à y
print(x <= y)  # vérifie si x est inférieur ou égal à y


# Fonctions utiles
print(type(x) == type(y))  # vérifie si x et y sont du même type
print(x % 2 == 0)  # vérifie si x est pair
print(x % 2 != 0)  # vérifie si x est impair
